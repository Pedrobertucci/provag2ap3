    public void listaTudaoEntrada(){
               try {
            //Registra JDBC driver
            Class.forName("com.mysql.jdbc.Driver");
 
            //Abrindo a conex√£o
              Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/portariaseguranca?zeroDateTimeBehavior=convertToNull","root", "vertrigo");
               
            //Executa a query de sele√ß√£o
            java.sql.Statement st = conn.createStatement();
            st.executeQuery("select m.nome,m.sobrenome,m.bloco,m.lote, e.data from entradas e \n" +
              "	inner join morador m "+
            "     where e.morador_idmorador = m.idmorador");
            ResultSet rs = st.getResultSet();
            TableModel table = DbUtils.resultSetToTableModel(rs);
            jTableUsers.setModel(table);
            //Lista os alunos no console
           
           
        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }//Fim try
    }
